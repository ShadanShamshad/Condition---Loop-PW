// Write a program that grades students based on their marks.


let marks = 51;

if (marks > 90 ){
    console.log("Grade A");
}else if (marks > 70 && marks <= 90){
    console.log("Grade B");
}else if (marks >= 50 && marks <= 70){
    console.log("Grade C");
}else {
    console.log("Grade F");
}


//Output
// Grade C
